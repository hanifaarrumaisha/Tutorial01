
public class Dosen {
	private int NIP;
	private String nama;
	
	public Dosen(int nIP, String nama) {
		NIP = nIP;
		this.nama = nama;
	}
	public int getNIP() {
		return NIP;
	}
	public void setNIP(int nIP) {
		NIP = nIP;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	
	
}
